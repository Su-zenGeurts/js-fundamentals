// Relational or comparison operators
console.log( 10 > 5 ) //more than
console.log( 2 < 5 ) // less than
console.log( 5 >= 5 ) // more than or equal
console.log( 4 <= 5 ) // less than or equal

// Equality operator
var x = 1
console.log(x == '1') //lose comparison
console.log(x === '1') //strict comparison